package com.argentinaprograma.testing;

public class Calculadora 
{
   public static Integer sumar(Integer numero1, Integer numero2) {
	   return numero1 + numero2;
   }
   
   public static Integer dividir(Integer numero1, Integer numero2) {
	   return numero1 / numero2;
   }
   
   public static boolean esMayorQue(Integer numero1, Integer numero2) {
	   return numero1 > numero2;
   }
}
